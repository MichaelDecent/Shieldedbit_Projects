"""This Module contains the basic operators """

def add(val1, val2):
    """The addition function """
    return (val1 + val2)


def sub(val1, val2):
    """The substration function """
    return (val1 - val2)


def mul(val1, val2):
    """The multiplication function """
    return (val1 * val2)


def div(val1, val2):
    """The Division Function """
    return (val1 / val2)


def mod(val1, val2):
    """ The Modulo function"""
    return (val1 % val2)